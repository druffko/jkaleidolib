package gg.massimo.jkaleidolib.lib;

public class BackgroundColor {

    // Text background colors
    public static final String black = "\u001B[40m";
    public static final String red = "\u001B[41m";
    public static final String green = "\u001B[42m";
    public static final String yellow = "\u001B[43m";
    public static final String blue = "\u001B[44m";
    public static final String purple = "\u001B[45m";
    public static final String cyan = "\u001B[46m";
    public static final String white = "\u001B[47m";

    public static final String lightBlack = "\u001B[100m";
    public static final String lightRed = "\u001B[101m";
    public static final String lightGreen = "\u001B[102m";
    public static final String lightYellow = "\u001B[103m";
    public static final String lightBlue = "\u001B[104m";
    public static final String lightMagenta = "\u001B[105m";
    public static final String lightCyan = "\u001B[106m";
    public static final String lightWhite = "\u001B[107m";
}
