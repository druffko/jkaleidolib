package gg.massimo.jkaleidolib.lib;

public class Background {

    private static final String reset = TextColor.reset;

    public static String black(String string) {
        return TextColor.black + string + reset;
    }

    public static String red(String string) {
        return TextColor.red + string + reset;
    }

    public static String green(String string) {
        return TextColor.green + string + reset;
    }

    public static String yellow(String string) {
        return TextColor.yellow + string + reset;
    }

    public static String blue(String string) {
        return TextColor.blue + string + reset;
    }

    public static String purple(String string) {
        return TextColor.purple + string + reset;
    }

    public static String cyan(String string) {
        return TextColor.cyan + string + reset;
    }

    public static String white(String string) {
        return TextColor.white + string + reset;
    }

    public static String lightBlack(String string) {
        return TextColor.lightBlack + string + reset;
    }

    public static String lightRed(String string) {
        return TextColor.lightRed + string + reset;
    }

    public static String lightGreen(String string) {
        return TextColor.lightGreen + string + reset;
    }

    public static String lightYellow(String string) {
        return TextColor.lightYellow + string + reset;
    }

    public static String lightBlue(String string) {
        return TextColor.lightBlue + string + reset;
    }

    public static String lightMagenta(String string) {
        return TextColor.lightMagenta + string + reset;
    }

    public static String lightCyan(String string) {
        return TextColor.lightCyan + string + reset;
    }

    public static String lightWhite(String string) {
        return TextColor.lightWhite + string + reset;
    }
}
